export const currentLang = writable(loadLang())

function loadLang() {
    try { return localStorage.getItem(LANG_KEY) || 'zh' } catch (e) { return 'zh' }
}

export function setLang(lang) {
    currentLang.set(lang)
    try { localStorage.setItem(LANG_KEY, lang) } catch (e) {}
}

export const t = derived(currentLang, ($lang) => {
    const msg = messages[$lang] || messages['zh']
    return (/** @type {string} */ key) => msg[key] || key
})
